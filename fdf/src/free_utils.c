#include "fdf.h"

void	free_map(t_map *map)
{
	int	i;

	if (!map)
		return ;
	if (map->points)
	{
		i = 0;
		while (i < map->height)
		{
			if (map->points[i])
				free(map->points[i]);
			i++;
		}
		free(map->points);
	}
	free(map);
}

void	free_split(char **split)
{
	int	i;

	if (!split)
		return ;
	i = 0;
	while (split[i])
	{
		free(split[i]);
		i++;
	}
	free(split);
}

void	free_fdf(t_fdf *fdf)
{
	if (!fdf)
		return ;
	if (fdf->cam)
		free(fdf->cam);
	if (fdf->map)
		free_map(fdf->map);
	if (fdf->img)
		mlx_delete_image(fdf->mlx, fdf->img);
	if (fdf->mlx)
		mlx_terminate(fdf->mlx);
}

void	cleanup_and_exit(t_fdf *fdf, char *msg)
{
	free_fdf(fdf);
	error_exit(msg);
}
